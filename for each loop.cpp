//print all the vector elements by using for each loop
#include<bits/stdc++.h>
using namespace std;

int main()
{
    vector<int>v={50,60,70,80,90,100};//vector declare and initialize
    for(int u:v)//for each loop
    {
        cout<<u<< " ";//print the value one by one
    }

    return 0;
}
